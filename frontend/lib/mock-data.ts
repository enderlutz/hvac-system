import type { ProposalRecord, TierOption, DashboardStats } from "./types";

function tier(
  name: string, brand: string, seer: string, tons: number, systemLabel: string,
  benefits: string[], warranty: string, installTime: string,
  equip: number, labor: number, adders: number, addersLabel: string,
): TierOption {
  const total = equip + labor + adders;
  return {
    tier_name: name,
    brand,
    seer_rating: seer,
    system_description: `${tons}-Ton ${systemLabel} — ${brand} ${seer}`,
    key_benefits: benefits,
    warranty,
    install_time: installTime,
    equipment_cost: `$${equip.toLocaleString()}`,
    labor_cost: `$${labor.toLocaleString()}`,
    adders_cost: addersLabel,
    total_price: `$${total.toLocaleString()}`,
    cash_discount_price: `$${Math.round(total * 0.95).toLocaleString()}`,
    is_placeholder: false,
  };
}

